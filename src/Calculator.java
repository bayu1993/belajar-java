class Calculator {
    /*create new method addition : for adding operation
    * @return int data type
    * @param int number1 and number2*/
    int addition(int number1, int number2){
        return number1 + number2;
    }
    /* create new method substraction : for substraction operation
    * @return int data type
    * @param int number1 and int number 2*/
    int subtraction(int number1, int number2){
        return number1 - number2;
    }
    /* create new method multiplication : for multiplication operation
    * @return int data type
    * @param int number1 and int number 2*/
    int multiplication(int number1, int number2){
        return number1 * number2;
    }
    /* create new method division : for division operation
    * @return double data type
    * @param int number1 and int number 2*/
    double division(int number1, int number2){
        return (double) number1 / number2;
    }


}
